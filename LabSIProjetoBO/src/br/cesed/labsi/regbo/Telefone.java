package br.cesed.labsi.regbo;

public class Telefone {
	
	private String numeroTelefoneRes;
	private String numeroCelular;
	private String email;

	public String getNumeroTelefoneRes() {
		return numeroTelefoneRes;
	}

	public void setNumeroTelefoneRes(String numeroTelefoneRes) {
		this.numeroTelefoneRes = numeroTelefoneRes;
	}

	public String getNumeroCelular() {
		return numeroCelular;
	}

	public void setNumeroCelular(String numeroCelular) {
		this.numeroCelular = numeroCelular;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
